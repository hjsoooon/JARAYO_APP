// Utility: get percentile range label
export const getPercentileRange = (value: number, monthData: PercentileRow): string => {
  if (value <= monthData.p3) return '3rd 미만';
  if (value <= monthData.p15) return '3rd~15th';
  if (value <= monthData.p50) return '15th~50th';
  if (value <= monthData.p85) return '50th~85th';
  if (value <= monthData.p97) return '85th~97th';
  return '97th 초과';
};

// Utility: calculate age in months (decimal)
export const getAgeMonths = (birthDate: string, measureDate: string): number => {
  const birth = new Date(birthDate);
  const measure = new Date(measureDate);
  return (measure.getFullYear() - birth.getFullYear()) * 12
    + (measure.getMonth() - birth.getMonth())
    + (measure.getDate() - birth.getDate()) / 30;
};
